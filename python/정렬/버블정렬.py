def bubble_sort(arr):
    n = len(arr)
    for i in range(n):
        # 마지막 i개의 요소는 이미 정렬되었으므로 무시합니다.
        for j in range(0, n - i - 1):
            # 인접한 두 요소를 비교하여 정렬되지 않은 경우 교환합니다.
            if arr[j] > arr[j + 1]:
                arr[j], arr[j + 1] = arr[j + 1], arr[j]
    return arr

